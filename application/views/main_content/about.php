              <h2>ICD-10</h2>
              <p>International Statistical Classification of Diseases and Related Health Problems revisi ke 10 atau disingkat dengan ICD-10,
                 buku ini di Indonesia dikenal dengan nama Klasifikasi Internasional Penyakit revisi ke 10 disingkat sebagai KIP/10,
                 adalah buku mengenai pengkodean atas penyakit dan tanda-tanda, gejala, temuan-temuan yang abnormal, keluhan, keadaan sosial dan eksternal
                 menyebabkan cedera atau penyakit, seperti yang diklasifikasikan oleh World Health Organization (WHO). </p>
              <p>Dalam pengkodean ini menetapkan lebih dari 155.000 memungkinkan berbagai kode dan memungkinkan yang banyak berasal dari pelacakan diagnosis
                 dan prosedur baru dengan perluasan yang signifikan pada kode-kode yang telah tersedia 17.000 pengkodean pada ICD-9 dan ICD-10 mulai bekerja
                 dari tahun 1983 dan dapat diselesaikan pada tahun 1992.</p>
